                    <h2 class="mt-12">Langkah 2: Buat Anggaran dengan Metode 50/30/20</h2>
                    <p>Setelah mengetahui rata-rata pemasukan bulanan, bagi alokasi dana Anda menggunakan aturan sederhana ini sebagai panduan:</p>
                    <div class="not-prose my-10 space-y-6">
                        <div class="p-6 bg-sky-500/10 border border-sky-500/20 rounded-2xl">
                            <h3 class="font-black text-2xl text-sky-300 mb-2">50% KEBUTUHAN (Needs)</h3>
                            <p class="text-sky-200/80 text-sm">Alokasi maksimal untuk semua hal yang tidak bisa Anda hidup tanpanya. Seperti sewa rumah, makanan pokok, tagihan listrik, transportasi kerja, dan cicilan utang LEGAL.</p>
                        </div>
