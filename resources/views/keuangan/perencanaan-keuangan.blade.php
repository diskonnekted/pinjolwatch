                    <h2 class="mt-12">Langkah 3: Tetapkan Tujuan Keuangan yang Jelas (SMART Goals)</h2>
                    <p>Tujuan yang tidak jelas hanya akan menjadi angan-angan. Gunakan metode SMART untuk membuat tujuan Anda lebih nyata:</p>
                    <ul class="list-disc pl-5 space-y-2">
                        <li><strong>Specific (Spesifik):</strong> Apa tepatnya yang ingin Anda capai? (Contoh: "Melunasi utang kartu kredit A")</li>
                        <li><strong>Measurable (Terukur):</strong> Berapa jumlahnya? (Contoh: "Sebesar Rp 5.000.000")</li>
                        <li><strong>Achievable (Dapat Dicapai):</strong> Apakah realistis dengan kondisi Anda?</li>
                        <li><strong>Relevant (Relevan):</strong> Apakah tujuan ini penting untuk Anda saat ini?</li>
                        <li><strong>Time-bound (Ada Batas Waktu):</strong> Kapan Anda ingin mencapainya? (Contoh: "Dalam 6 bulan ke depan")</li>
                    </ul>
                    <p><strong>Contoh SMART Goal:</strong> "Saya akan melunasi utang kartu kredit A sebesar Rp 5.000.000 dalam 6 bulan dengan cara menyisihkan Rp 850.000 setiap bulan dari pos 'Keinginan'."</p>

