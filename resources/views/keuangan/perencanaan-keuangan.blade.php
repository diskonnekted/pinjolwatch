                            <h3 class="font-black text-2xl text-amber-300 mb-2">30% KEINGINAN (Wants)</h3>
                            <p class="text-amber-200/80 text-sm">Alokasi untuk gaya hidup. Seperti makan di luar, langganan Netflix, membeli gadget baru, atau liburan. Ini adalah pos pertama yang harus dipotong jika Anda ingin mempercepat pelunasan utang atau menabung.</p>
                        </div>
                        <div class="p-6 bg-emerald-500/10 border border-emerald-500/20 rounded-2xl">
