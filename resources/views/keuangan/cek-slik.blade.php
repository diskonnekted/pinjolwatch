                    <h2 class="mt-12">Langkah-Langkah Cek SLIK Online via iDebKu</h2>
                    <p>Proses ini sepenuhnya GRATIS dan bisa dilakukan dari rumah. Siapkan dokumen berikut dalam bentuk digital (foto/scan):</p>
                     <ul>
                        <li><strong>Debitur Perseorangan:</strong> Foto KTP.</li>
                        <li><strong>Debitur Meninggal Dunia:</strong> Foto KTP ahli waris, foto Surat Keterangan Kematian.</li>
                        <li><strong>Badan Usaha:</strong> NPWP, Akta Pendirian, Identitas Pengurus.</li>
                    </ul>
                    <p>Berikut langkahnya:</p>
                    <ol>
                        <li>Buka browser dan kunjungi situs resmi OJK: <a href="https://idebku.ojk.go.id" target="_blank" rel="noopener noreferrer">https://idebku.ojk.go.id</a></li>
                        <li>Klik tombol <strong>"Pendaftaran"</strong> di halaman utama.</li>
                        <li><strong>Cek Ketersediaan Layanan:</strong> Masukkan jenis debitur (perorangan/badan usaha) dan nomor identitas (NIK). Isi captcha dan klik "Selanjutnya".</li>
                        <li><strong>Isi Data Diri:</strong> Lengkapi formulir data diri dengan akurat. Pastikan email dan nomor HP aktif.</li>
                        <li><strong>Unggah Dokumen:</strong> Upload foto/scan dokumen yang sudah Anda siapkan tadi. Pastikan foto jelas dan tidak buram.</li>
                        <li><strong>Ajukan Permohonan:</strong> Setelah semua data terisi, klik tombol "Ajukan Permohonan".</li>
                        <li><strong>Tunggu Email dari OJK:</strong> Anda akan menerima email dari OJK yang menginformasikan nomor pendaftaran. Proses verifikasi biasanya memakan waktu 1-3 hari kerja.</li>
                        <li><strong>Unduh Laporan iDeb:</strong> Jika data Anda valid, OJK akan mengirimkan hasil laporan iDeb SLIK ke email Anda dalam format PDF. Selesai!</li>
                    </ol>

                     <h2 class="mt-12">Nama Saya Jelek di SLIK, Apa yang Harus Dilakukan?</h2>
                     <p>Jangan panik. Ini bisa diperbaiki, meski butuh waktu dan komitmen.</p>
                      <ul class="list-disc pl-5 space-y-3">
                        <li><strong>Lakukan Pelunasan:</strong> Satu-satunya cara adalah melunasi utang yang menyebabkan skor Anda jelek. Prioritaskan utang yang sudah masuk Kol-3 ke atas.</li>
                        <li><strong>Minta Surat Keterangan Lunas:</strong> Setelah lunas, WAJIB minta Surat Keterangan Lunas dari kreditor sebagai bukti sah.</li>
                        <li><strong>Update Data ke OJK:</strong> Hubungi kembali kreditor dan minta mereka untuk segera memperbarui data Anda di SLIK OJK. Proses ini bisa memakan waktu beberapa bulan.</li>
                        <li><strong>Pantau Secara Berkala:</strong> Cek kembali SLIK Anda 3-6 bulan kemudian untuk memastikan skor Anda sudah membaik menjadi Kol-1.</li>
                    </ul>
