                    <h2>Apa itu Skor Kolektibilitas (KOL)?</h2>
                    <p>SLIK akan menampilkan skor kredit Anda yang disebut Kolektibilitas (Kol). Skor ini terdiri dari 5 tingkatan:</p>
                    <ul>
                        <li><strong>Kol-1 (Lancar):</strong> TOP! Anda membayar cicilan tepat waktu. Ini yang dicari bank.</li>
                        <li><strong>Kol-2 (Dalam Perhatian Khusus/DPK):</strong> Ada tunggakan 1-90 hari. Lampu kuning bagi bank.</li>
                        <li><strong>Kol-3 (Kurang Lancar):</strong> Tunggakan 91-120 hari.</li>
                        <li><strong>Kol-4 (Diragukan):</strong> Tunggakan 121-180 hari.</li>
                        <li><strong>Kol-5 (Macet):</strong> Tunggakan lebih dari 180 hari. Ini adalah 'blacklist' yang sebenarnya. Sulit sekali mendapat pinjaman lagi jika skor Anda Kol-5.</li>
                    </ul>
                    <p class="text-sm"><strong>Penting:</strong> Pinjol ILEGAL tidak memiliki akses dan tidak bisa melaporkan ke SLIK OJK. Hanya pinjaman dari lembaga legal (OJK) yang akan tercatat.</p>

                    <div class="not-prose my-10 p-6 bg-green-500/10 border border-green-500/20 rounded-2xl">
                        <h3 class="font-bold text-lg mb-2 text-green-300">Mengapa Cek SLIK itu Penting?</h3>
                        <ul class="list-disc pl-5 space-y-2 text-sm text-green-200/80">
                            <li><strong>Memastikan Nama Bersih:</strong> Verifikasi apakah ada pinjaman atas nama Anda yang tidak Anda ketahui (fraud).</li>
                            <li><strong>Alat Negosiasi:</strong> Mengetahui utang mana yang paling merusak skor kredit Anda untuk diprioritaskan.</li>
                            <li><strong>Merencanakan Masa Depan:</strong> Syarat utama untuk mengajukan KPR, kredit usaha (KUR), atau pinjaman besar lainnya di masa depan.</li>
                        </ul>
