### 💡 Bedah Cerita: Bagaimana Keluarga Menjadi Support System?

Jika Anda adalah anggota keluarga (Ayah, Ibu, Suami, Istri, Kakak, atau Adik), cerita Raka mengajarkan 4 pilar utama dukungan mental:

#### 1. Validasi Emosi (Bukan Logika Dulu)
Saat korban sedang panik/depresi, otaknya tidak bisa memproses nasihat keuangan.
*   ❌ **Salah:** *"Kenapa sih pinjam? Kan rugi!"*
*   ✅ **Benar:** *"Ibu/Ayah lihat kamu ketakutan. Ini pasti berat banget ya? Kita hadapi bareng."*
    > **Efek:** Kortisol (hormon stres) turun. Korban merasa aman secara emosional.

#### 2. Menjadi "Tameng" Sosial
Korban pinjol sering ketakutan pada stigma sosial (tetangga, kantor, teman).
*   **Tindakan:** Keluarga harus siap "menjaga gawang". Jawab telepon yang mengancam, hadapi tamu yang mengganggu, dan lindungi privasi korban dari gosip.
    > **Efek:** Korban merasa tidak harus bertarung sendirian melawan dunia.

#### 3. Pisahkan "Kasus" dari "Orang"
Ingatkan terus bahwa yang bermasalah adalah **sistem pinjol/bunganya**, bukan **harga diri si korban**.
*   **Katakan:** *"Kamu anak yang baik yang sedang kena jebakan sistem. Ini bukan akhir dari segalanya."*
    > **Efek:** Memulihkan harga diri (self-esteem) yang sering hancur akibat teror DC.

#### 4. Dampingi Proses, Jangan Bayarkan Semua (Jika Tidak Mampu)
Keluarga tidak harus melunasi utang jika memang tidak sanggup (ini justru bisa memicu masalah baru).
*   **Fokuskan pada:** Mendampingi korban membuat anggaran, menemani ke LBH/OJK, atau sekadar menemani makan malam tanpa membahas utang.
    > **Efek:** Korban mendapat dukungan strategis dan emosional tanpa merasa menjadi beban finansial total bagi keluarga.

